package controller.commands.controller;

import java.io.IOException;

/**
 * Represents the controller interface
 * that hosts the startProgram() method
 * to start the Image Processing System.
 */
public interface IController {

  /**
   * Represents a method that starts the Image
   * Processing system.
   * @throws IOException if the program cannot be transmitted.
   */
  void startProgram() throws IOException;



}

